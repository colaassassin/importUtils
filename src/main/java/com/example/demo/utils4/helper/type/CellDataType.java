package com.example.demo.utils4.helper.type;


public enum CellDataType {
    /**
     *  string  cell type
     */
    STRING,
    /**
     *  numeric  cell type
     */
    NUMERIC,
    /**
     *  formula  cell type, for excel
     */
    FORMULA,
    /**
     *  blank cell type
     */
    BLANK,
    /**
     *  boolean cell type
     */
    BOOLEAN,
    /**
     *  error cell type,  for excel
     */
    ERROR,
    /**
     *  no cell type
     */
    NONE;
}
